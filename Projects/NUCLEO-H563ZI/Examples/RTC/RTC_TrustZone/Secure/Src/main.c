{
  for (uint32_t i = 0; i < RTC_BKP_NB; i++)
  {
    /* Write backup register */
    HAL_RTCEx_BKUPWrite(&hrtc, i, i);
    /* Read backup register */
    if (HAL_RTCEx_BKUPRead(&hrtc, i) != i)
    {
      Error_Handler();
    }
  }
}

